package main;

public class Contobancario {

	private int conto;
	private int deposito;
	private int prelievo;
	
	public int Conto() {
		return conto;
	}
	
	public int Deposito() {
		return deposito;
	}
	
	public int Prelievo() {
		return prelievo;		
	}
	
	public void setConto(){
		this.conto = conto;
	}
	public void setDeposito() {
		this.deposito = deposito;
	}
	public void setPrelievo() {
		this.prelievo = prelievo;
	}
	
	
	
	
	
	

}
